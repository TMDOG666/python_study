#### **1. 定义一个最简单的函数**

# **语法结构：**

# ```python
# def function_name():
#     # 函数体 (indented block of code)
#     # 这里是函数要执行的代码
# ```
# 
# * `def`：关键字，告诉 Python 你正在定义一个函数。
# * `function_name`：你给函数起的名字。命名规则和变量一样（小写字母和下划线），并且名字要有意义。
# * `()`：圆括号，即使没有信息要传递，它也是必需的。
# * `:`：冒号，表示函数定义的开始。
# * **缩进的代码块**：所有属于这个函数的代码都必须缩进。